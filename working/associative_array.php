<?php 
// working with Associative Arrays

// associative array use a string as a key
$name['first'] = 'Julia';
$name['last'] = 'Roberts';
$name['age'] = 35;
$name['occupation'] = 'Actress';



// you can then access the elements by key
printf('<br /> Full Name: %s %s', $name['first'], $name['last']);


?>